package chap_02;

public class _01_Operator {
    public static void main(String[] args) {


        // 산술 연산자
        System.out.println(4 + 2); // 6  드하기
        System.out.println(4 - 2); // 2  빼기
        System.out.println(4 * 2); // 8  곱하기
        System.out.println(4 / 2); // 2  나누기
        System.out.println(5 / 2); // 2.5 이지만 정수기준이기때문에 정수로 나옴 2
        System.out.println(2 / 4); // 0
        System.out.println(4 % 2); // 0
        System.out.println(5 % 2); // 1 나머지 연산

        //우선 순위 연산
        System.out.println(2 + 2 * 2);














    }

}
